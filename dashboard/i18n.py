def normalize_lang(lang: Any) -> str:
    value = str(lang or "").strip().lower()
    return value if value in SUPPORTED_LANGS else DEFAULT_LANG


def t(lang: Any, key: str, **kwargs: Any) -> str:
    resolved_lang = normalize_lang(lang)
    translated = _TRANSLATIONS.get(resolved_lang, {}).get(key)
    if translated is None:
        translated = _TRANSLATIONS[DEFAULT_LANG].get(key, key)
    if kwargs:
        return translated.format(**kwargs)
    return translated


def stage_label(stage: str, lang: Any) -> str:
    resolved_lang = normalize_lang(lang)
    labels = _STAGE_TRANSLATIONS.get(str(stage), {})
    return labels.get(resolved_lang, str(stage))
